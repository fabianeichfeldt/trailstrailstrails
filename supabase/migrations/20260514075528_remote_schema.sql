CREATE OR REPLACE FUNCTION "public"."sync_profile_from_auth_users"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$BEGIN
  -- On INSERT or UPDATE of auth.users, upsert into public.profiles
  INSERT INTO public.profiles (id, display_name, avatar_url, created_at)
  VALUES (
    NEW.id,
    (NEW.raw_user_meta_data ->> 'name')::text,
    (NEW.raw_user_meta_data ->> 'avatar_url')::text,
    NEW.created_at
  )
  ON CONFLICT (id) DO UPDATE
  SET
    display_name = EXCLUDED.display_name,
    avatar_url = EXCLUDED.avatar_url,
    created_at = EXCLUDED.created_at;

  RETURN NEW;
END;$$;


