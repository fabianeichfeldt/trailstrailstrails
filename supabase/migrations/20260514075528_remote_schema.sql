CREATE OR REPLACE FUNCTION "public"."can_edit_spot"("p_spot_id" "text") RETURNS boolean
    LANGUAGE "sql" STABLE SECURITY DEFINER
    AS $$SELECT is_admin()
OR (get_my_role() = 'trailcrew'                                                                   AND EXISTS (        
            SELECT 1 FROM trailcrew_spots                  
            WHERE user_id = auth.uid() AND spot_id::text = p_spot_id ))$$;


ALTER FUNCTION "public"."can_edit_spot"("p_spot_id" "text") OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."get_my_role"() RETURNS "public"."user_role"
    LANGUAGE "sql" STABLE SECURITY DEFINER
    AS $$SELECT COALESCE(
      (SELECT role FROM user_roles WHERE user_id = auth.uid()),                                                                                                                                                                         
      'user'::user_role
    )$$;


